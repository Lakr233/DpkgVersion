    #expect(Version.compare("1.0.0+2", "1.0.0+1") > 0)
    #expect(Version.compare("1.0.0+", "1.0.0+") == 0)

    for testCase in testCases {
        let testData = try! JSONDecoder().decode(TestData.self, from: testCase.data(using: .utf8)!)

        let lhs = testData.lhs
        let rhs = testData.rhs

        if lhs == rhs { #expect(testData.result == 0) }

        let v_lhs = Version.parse(from: lhs)!
        let v_rhs = Version.parse(from: rhs)!
        let result = Version.compare(v_lhs, v_rhs)
        #expect(result == testData.result)
    }
}

